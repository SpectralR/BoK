import React from "react";

// reactstrap components
import { Button, Container } from "reactstrap";

// core components

function RulesHeader() {
  let pageHeader = React.createRef();

  React.useEffect(() => {
    if (window.innerWidth < 991) {
      const updateScroll = () => {
        let windowScrollTop = window.pageYOffset / 3;
        pageHeader.current.style.transform =
          "translate3d(0," + windowScrollTop + "px,0)";
      };
      window.addEventListener("scroll", updateScroll);
      return function cleanup() {
        window.removeEventListener("scroll", updateScroll);
      };
    }
  });

  return (
    <>
      <div
        style={{
          backgroundImage:
            "url(" + require("assets/img/il_mheg_underwater.png") + ")",
        }}
        className="page-header"
        data-parallax={true}
        ref={pageHeader}
      >
        <div className="filter" />
        <Container>
          <div className="motto text-center">
            <h1>Rules</h1>
            <h3>DISCLAIMER: Free Company rules will inevitably change over time. Members are expected to accept and adhere to new rules regardless of their personal opinions. Be mindful that the fc staff tries to make decisions that are best for the guild as a whole and for the long term</h3>
          </div>
        </Container>
      </div>
    </>
  );
}

export default RulesHeader;
